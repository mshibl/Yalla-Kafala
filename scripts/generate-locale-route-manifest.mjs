/**
 * Writes `src/lib/sitemap/locale-route-manifest.generated.ts` by scanning `src/app/[locale]`.
 * Run from repo root via `node scripts/generate-locale-route-manifest.mjs` (see package.json).
 * Serverless (e.g. Vercel) often has no `src/` tree at runtime, so the sitemap imports this file instead of using fs.
 */
import { readdirSync, mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = fileURLToPath(new URL(".", import.meta.url));
const repoRoot = join(__dirname, "..");
const localeRoot = join(repoRoot, "src", "app", "[locale]");
const outFile = join(
  repoRoot,
  "src",
  "lib",
  "sitemap",
  "locale-route-manifest.generated.ts",
);

function isDynamicSegment(segment) {
  return segment.startsWith("[") && segment.endsWith("]");
}

function isRouteGroup(segment) {
  return segment.startsWith("(") && segment.endsWith(")");
}

function discoverLocaleRoutes() {
  const staticPaths = [];
  const dynamicKeys = new Set();

  function walk(dir, segments) {
    const entries = readdirSync(dir, { withFileTypes: true });
    const hasPage = entries.some((e) => e.isFile() && e.name === "page.tsx");

    if (hasPage) {
      const hasDynamic = segments.some(isDynamicSegment);
      if (hasDynamic) {
        dynamicKeys.add(segments.join("/"));
      } else {
        staticPaths.push(segments.length ? segments.join("/") : "");
      }
    }

    for (const e of entries) {
      if (!e.isDirectory()) continue;
      const child = join(dir, e.name);
      if (isRouteGroup(e.name)) {
        walk(child, segments);
      } else {
        walk(child, [...segments, e.name]);
      }
    }
  }

  walk(localeRoot, []);
  staticPaths.sort();
  return {
    staticPaths,
    dynamicKeys: [...dynamicKeys].sort(),
  };
}

const { staticPaths, dynamicKeys } = discoverLocaleRoutes();

const banner = `/* eslint-disable */
/**
 * AUTO-GENERATED by scripts/generate-locale-route-manifest.mjs — do not edit.
 * Regenerate after adding/removing routes under src/app/[locale]:
 *   pnpm run sitemap:manifest
 */
`;

const content = `${banner}
export const LOCALE_STATIC_PATHS = ${JSON.stringify(staticPaths, null, 2)} as const;

export const LOCALE_DYNAMIC_ROUTE_KEYS = ${JSON.stringify(dynamicKeys, null, 2)} as const;
`;

mkdirSync(dirname(outFile), { recursive: true });
writeFileSync(outFile, content, "utf8");
console.log("Wrote", outFile);
